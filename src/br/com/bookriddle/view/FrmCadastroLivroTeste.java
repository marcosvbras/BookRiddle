    @Override
    public boolean validarCampos() {
        boolean resultado = false;

        try {
            int paginas = Integer.parseInt(txt_paginas.getText());
            int quantidade = Integer.parseInt(txt_quantidade.getText());
            int ano = Integer.parseInt(txt_ano.getText());
            int prateleira = Integer.parseInt(txt_prateleira.getText());
            resultado = true;
        } catch (Exception e) {
            resultado = false;
        }

        if (!txt_isbn.getText().equals("") && !txt_titulo.getText().equals("") && autor != null
                && area != null && !txt_edicao.getText().equals("") && editora != null && !txt_paginas.getText().equals("") 
                && !txt_quantidade.equals("") && !txt_ano.getText().equals("") && resultado) {

            return true;
        } else {
            return false;
        }
    }
    
