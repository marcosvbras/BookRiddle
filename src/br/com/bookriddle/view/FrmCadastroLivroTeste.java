    private void btn_cadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_cadastrarMouseClicked
        if (validarCampos()) {
            Livro l = new Livro();
            l.setIsbn(txt_isbn.getText());
            l.setTitulo(txt_titulo.getText());
            l.setAutorId(autor.getId());
            l.setAreaId(area.getId());
            l.setEdicao(txt_edicao.getText());
            l.setEditoraId(editora.getId());
            l.setPaginas(Integer.parseInt(txt_paginas.getText()));
            l.setQuantidade(Integer.parseInt(txt_quantidade.getText()));
            l.setAno(Integer.parseInt(txt_ano.getText()));
            l.setStatus(Livro.STATUS_ATIVO);

            if (txt_armario.getText().equals("")) {
                l.setArmario(null);
                l.setPrateleira(null);
            } else {
                l.setArmario(txt_armario.getText());
                l.setPrateleira(txt_prateleira.getText());
            }

            l.setUrl(destination_path);

            LivroDao dao = new LivroDao(l);
            boolean resultado = dao.inserir();
            if (resultado) {
                JOptionPane.showMessageDialog(null, "Livro cadastrado com sucesso.");
            }
            it.transferirImagem();
            imagem_selecionada = null;
            busca.setList(dao.buscarTodos("where status_livro != 0 order by titulo"));
            dao.closeConnection();
        } else {
            JOptionPane.showMessageDialog(null, "Verifique se todos os campos foram preenchidos corretamente");
        }
    }//GEN-LAST:event_btn_cadastrarMouseClicked

