    private void btn_excluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_excluirMouseClicked
        if (fun != null) {
            int result = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir este funcionário?");
          
            if (result == 0) {
                FuncionarioDao dao = new FuncionarioDao(fun);
                dao.excluir();
                busca.setList(dao.buscarTodos("where status_funcionario != 0 order by nome"));
                dao.closeConnection();
                dispose();
            }
        }
    }//GEN-LAST:event_btn_excluirMouseClicked

