    private void preencherCampos() {
        txt_matricula.setText(fun.getMatricula());
        txt_nome.setText(fun.getNome());
        txt_cpf.setText(fun.getCpf());
        txt_telefone.setText(fun.getTelefone());
        txt_email.setText(fun.getEmail());
        fun.setTempMatricula(fun.getMatricula());
    }

    @Override
    public boolean validarCampos() {
        boolean resultado = false;
        
        try{
            int matricula = Integer.parseInt(txt_matricula.getText());
            resultado = true;
        } catch(Exception e) {
            
        }
        
        if (!txt_nome.getText().equals("") && !txt_matricula.getText().equals("") && txt_cpf.getText().length() == 14
                && !txt_email.getText().equals("") && txt_telefone.getText().length() >= 14 && resultado) {
            return true;
        } else {
            return false;
        }
    }

