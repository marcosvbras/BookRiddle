    private void btn_salvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_salvarMouseClicked
        if (validarCampos()) {
            if (action == FrmEditarLivro.ACTION_INSERT) {
                fun = new Funcionario();
                fun.setMatricula(txt_matricula.getText());
                fun.setNome(txt_nome.getText());
                fun.setEmail(txt_email.getText());
                fun.setCpf(txt_cpf.getText());
                fun.setTelefone(txt_telefone.getText());
                fun.setStatus(Funcionario.STATUS_ATIVO);
                FuncionarioDao dao = new FuncionarioDao(fun);
                boolean resultado = dao.inserir();
                if (resultado) {
                    JOptionPane.showMessageDialog(null, "Funcionário cadastrado com sucesso.");
                } else {
                    JOptionPane.showMessageDialog(null, "Digite uma matrícula válida");
                }
                busca.setList(dao.buscarTodos("where status_funcionario != 0 order by nome"));
                dao.closeConnection();
            } else {
                fun.setMatricula(txt_matricula.getText());
                fun.setNome(txt_nome.getText());
                fun.setEmail(txt_email.getText());
                fun.setCpf(txt_cpf.getText());
                fun.setTelefone(txt_telefone.getText());
                fun.setStatus(Funcionario.STATUS_ATIVO);
                FuncionarioDao dao = new FuncionarioDao(fun);
                boolean resultado = dao.editar();
                if (resultado) {
                    JOptionPane.showMessageDialog(null, "Funcionário atualizado com sucesso.");
                }
                busca.setList(dao.buscarTodos("where status_funcionario != 0 order by nome"));
                dao.closeConnection();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Verifique se todos os campos foram preenchidos corretamente");
        }
    }//GEN-LAST:event_btn_salvarMouseClicked

